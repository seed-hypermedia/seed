import {readFileSync, writeFileSync, readdirSync} from 'node:fs'
import {join} from 'node:path'
const SRC = 'schemas'
const OUT = 'frontend/packages/ui/src/onyx/onyx-schemas.generated.ts'
const files = readdirSync(SRC).filter((f) => f.endsWith('.json') && f !== 'schemas.lock.json')
const schemas = {}
for (const f of files.sort()) {
  const name = f.replace(/\.json$/, '')
  schemas[name] = JSON.parse(readFileSync(join(SRC, f), 'utf8'))
}
const lock = JSON.parse(readFileSync(join(SRC, 'schemas.lock.json'), 'utf8'))
const manifest = lock.schemas || {}
const header = `// AUTO-GENERATED by scripts/gen-onyx (do not edit by hand).
// The Onyx reference schemas (schemas/*.json) bundled for in-app browsing +
// validation, plus the published CID manifest (schemas.lock.json).
// Regenerate: node scripts/gen-onyx.mjs\n\n`
const body =
  `export const ONYX_AUTHORITY: ReadonlyArray<readonly [string, string]> = ${JSON.stringify([['onyx-', 'hyper.media'], ['hypermedia-', 'seed.hyper.media'], ['example-', 'example.com']])} as const\n\n` +
  `export const ONYX_MANIFEST: Record<string, string> = ${JSON.stringify(manifest, null, 0)}\n\n` +
  `/** filename (no .json) -> schema object, e.g. "onyx-map-schema", "example-person", "hypermedia-change". */\n` +
  `export const ONYX_SCHEMAS: Record<string, any> = ${JSON.stringify(schemas)}\n`
writeFileSync(OUT, header + body)
console.log(`wrote ${OUT}: ${Object.keys(schemas).length} schemas, ${Object.keys(manifest).length} manifest entries`)
